CREATE OR REPLACE VIEW RATE_AND_TTF_BENCHMARKS_V (CMSA_CODE, BENCHMARK_TYPE, STD_OCCUPATION_ID, PLACE_ID, UNIT, BENCHMARK_25_PERCENTILE, BENCHMARK_50_PERCENTILE, BENCHMARK_75_PERCENTILE, LOAD_DATE, EFFECTIVE_DATE, END_DATE) AS 
select x.cmsa_code, b.BENCHMARK_TYPE,b.STD_OCCUPATION_ID,b.PLACE_ID,b.UNIT,b.BENCHMARK_25_PERCENTILE,b.BENCHMARK_50_PERCENTILE,
b.BENCHMARK_75_PERCENTILE,b.LOAD_DATE,b.EFFECTIVE_DATE,b.END_DATE from RATE_AND_TTF_BENCHMARKS b, DM_CMSA_PLACE_XREF x
where x.parent_std_place_id = b.place_id
/
